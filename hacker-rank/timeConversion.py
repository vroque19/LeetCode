def timeConversion(s):
    # Write your code here
    hours = s[:2]
    if int(hours) < 12 and "PM" in s:
        hours = int(hours) + 12
    elif int(hours) == 12 and "AM" in s:
        hours = "00"
    return str(hours) + s[2:8]
    

